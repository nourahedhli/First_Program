import math


class Calculator:

    def __init__(self):
        pass

    def addition(self, a, b):
        return a + b

    def subtraction(self, a, b):
        return a - b

    def multiplication(self, a, b):
        return a * b

    def divison (self, a,b):
        return a/b

    def square_root (self,a):

        return math.sqrt(a)

    def square (self,a):
        return a ** 2